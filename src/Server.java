    private void initReciveMessageThread() {
        reciveMessageThread = new Thread() {
            @Override
            public void run() {
                while (true) {
                    try {
                        reciveMessage = dis.readUTF();
                        addNewMessage("client");
                    } catch (IOException ex) {
                        JOptionPane.showMessageDialog(rootPane, "Client has been stopped");
                        try {
                            dos.close();
                            os.close();
                            dis.close();
                            is.close();
                            socket.close();
                            serverSocket.close();
                        } catch (IOException ex1) {
                        }
                        txtMessage.setEditable(false);
                        btnSend.setEnabled(false);
                        reciveMessageThread.stop();
                    }
                }
            }

        };
    }

    private void addNewMessage(String user) {
        JPanel pnMsg = new JPanel();
        pnMsg.setMaximumSize(new Dimension(412, 22));
        pnMsg.setBackground(Color.WHITE);

        JLabel lblMsg = new JLabel();
        lblMsg.setFont(new Font("tahoma", Font.BOLD, 14));
        lblMsg.setVerticalAlignment(JLabel.CENTER);

        switch (user) {
            case "client":
                pnMsg.setLayout(new FlowLayout(FlowLayout.LEFT));
                lblMsg.setText(reciveMessage);
                lblMsg.setForeground(Color.decode("#FC271C"));
                break;
            case "server":
                pnMsg.setLayout(new FlowLayout(FlowLayout.RIGHT));
                lblMsg.setText(txtMessage.getText());
                lblMsg.setForeground(Color.decode("#259BD1"));
                break;
        }
        pnMsg.add(lblMsg);
        pnChat.add(pnMsg);
        pnChat.revalidate();
        pnChat.repaint();
    }

