    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        if (txtMessage.getText().equals("") || serverSocket == null) {
            return;
        }
        addNewMessage("server");
        try {
            dos.writeUTF(txtMessage.getText());
            dos.flush();
            txtMessage.setText("");
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSendActionPerformed

    private void btnConnectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConnectActionPerformed
        try {
            if (serverSocket == null) {
                serverSocket = new ServerSocket(Integer.parseInt(txtPort.getText()));
                JOptionPane.showMessageDialog(this, "Server is turning on \nWaiting for client connection");
                socket = serverSocket.accept();
                JOptionPane.showMessageDialog(this, "Connect successful");
                txtMessage.setEditable(true);
                btnSend.setEnabled(true);
                is = socket.getInputStream();
                dis = new DataInputStream(is);
                os = socket.getOutputStream();
                dos = new DataOutputStream(os);
                reciveMessageThread.start();
            } else {
                JOptionPane.showMessageDialog(this, "Server is already turned");
            }
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Connect failed");
        }
    }//GEN-LAST:event_btnConnectActionPerformed

    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        try {
            if (dos != null) {
                dos.close();
            }
            if (os != null) {
                os.close();
            }
            if (dis != null) {
                dis.close();
            }
            if (is != null) {
                is.close();
            }
            if (reciveMessageThread != null) {
                reciveMessageThread.stop();
            }
            if (socket != null) {
                socket.close();
            }
            if (serverSocket != null) {
                serverSocket.close();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_formWindowClosing

